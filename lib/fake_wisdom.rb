class FakeWisdom

  attr_reader :maxlength, :multiline

  def initialize opt={}
      @maxlength = (opt[:maxlength] || opt[:max] || 80).to_i
      @multiline = !!(opt[:multiline] || opt[:yn] || false)
      
      filename = opt[:file] || opt[:dict] || opt[:dictionary] || __FILE__
      list = nil
      File.open(filename){ |f| list = f.read().split.map(&:strip) }
      if filename == __FILE__ then
          while '__END__' != list.shift do;end
      end
      list = list.select{|e| e }.uniq

      @list_all, @list_lastable = [], []
      list.select{|line|
          line.length < @maxlength
      }.each do |line|
          @list_all << line
          @list_lastable << line if line =~ /。$/
      end
      raise "@list_lastable is empty" if @list_lastable.length <= 0
      srand(Time.new.to_i)
  end

  def get n=1
      Array.new(n.to_i).map{|e|
          line = _get
          if @multiline then
                length, yn = (@maxlength - line.length), "\n"
                line = line.gsub(/(。)/){
                    add = (rand(3) == 0) && (length > 0)
                    length -= (add ? 1 : 0)
                    "#{$1}#{add ? yn : ''}"
                }
          end
          line.strip
      }
  end
  def _get
      str = _pick @list_lastable
      (100).times do
          add = _pick @list_all
          res = add + str
          if res.length > @maxlength && str.length <= @maxlength then
              return str
          elsif res.length <= @maxlength then
              str = res
          end
      end
  end

  def _pick collection
      collection[rand(collection.length)]
  end

end

